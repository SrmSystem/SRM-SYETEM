package com.qeweb.scm.purchasemodule.web.vo;

public class CapacityViewVO {
	
	private String capacityId;
	private String vendorName;
	private String vendorIds;
	private String codes;
	private String abolished;
	
	
	public String getCapacityId() {
		return capacityId;
	}
	public void setCapacityId(String capacityId) {
		this.capacityId = capacityId;
	}
	public String getVendorName() {
		return vendorName;
	}
	public void setVendorName(String vendorName) {
		this.vendorName = vendorName;
	}
	public String getVendorIds() {
		return vendorIds;
	}
	public void setVendorIds(String vendorIds) {
		this.vendorIds = vendorIds;
	}
	public String getCodes() {
		return codes;
	}
	public void setCodes(String codes) {
		this.codes = codes;
	}
	public String getAbolished() {
		return abolished;
	}
	public void setAbolished(String abolished) {
		this.abolished = abolished;
	}

}
